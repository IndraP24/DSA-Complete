package Strings;

public class S4_PrettyPrint {
    public static void main(String[] args) {
        float a  = 46.29834f;
        System.out.printf("Formatted Num is %.2f", a);

        System.out.printf("\nPie: %.3f", Math.PI);

        System.out.printf("Hello, my name in %s, and I am a %s.", "Indra", "student");
    }
}
